#include <REGX52.H>

void main()
{
	//while循环中参数为就一直循环
	while(1)
	{
			if(P3_1 == 0 || P3_0 == 0) //比较p31（独立按键）的电平
			{
				P2_0=0; //按下点亮
			}
			else
			{
				P2_0=1;//松开灭掉
			
			}
			
			
			if(P3_0 == 0) //比较p30（独立按键）的电平
			{
				P2_1=0; //按下点亮
			}
			else
			{
				P2_1=1;//松开灭掉
			
			}
			
			
			if(P3_2 == 0) //比较p32（独立按键）的电平
			{
				P2_2=0; //按下点亮
			}
			else
			{
				P2_2=1;//松开灭掉
			
			}
	}

}
	